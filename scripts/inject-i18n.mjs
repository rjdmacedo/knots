// Map locale key to filename
const localeToFile = {
  'pt-BR': 'pt-BR.json',
  es: 'es.json',
  ca: 'ca.json',
  'de-DE': 'de-DE.json',
  'fr-FR': 'fr-FR.json',
  'it-IT': 'it-IT.json',
  'nl-NL': 'nl-NL.json',
  'pl-PL': 'pl-PL.json',
  'cs-CZ': 'cs-CZ.json',
  ro: 'ro.json',
  'ru-RU': 'ru-RU.json',
  'ua-UA': 'ua-UA.json',
  'tr-TR': 'tr-TR.json',
  fi: 'fi.json',
  'ja-JP': 'ja-JP.json',
  'zh-CN': 'zh-CN.json',
  'zh-TW': 'zh-TW.json',
}

for (const [locale, filename] of Object.entries(localeToFile)) {
  const filePath = join(messagesDir, filename)
  const content = JSON.parse(readFileSync(filePath, 'utf-8'))

  if (!content.Friends) {
    console.error(`No Friends key in ${filename}, skipping.`)
    continue
  }

  const t = translations[locale]
  if (!t) {
    console.error(`No translations for ${locale}, skipping.`)
    continue
  }

  // Merge new keys into Friends
  content.Friends.List = t.List
  content.Friends.Timeline = t.Timeline
  content.Friends.PaymentDetail = t.PaymentDetail
  content.Friends.DirectExpense = t.DirectExpense
  content.Friends.SettleAll = t.SettleAll

  writeFileSync(filePath, JSON.stringify(content, null, 2) + '\n', 'utf-8')
  console.log(`✓ Updated ${filename}`)
}

console.log('\nDone! All locale files updated.')
